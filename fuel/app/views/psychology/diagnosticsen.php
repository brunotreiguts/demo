<h2>ANGĻU VALODAS TEKSTAM PAREDZĒTAIS SKATS</h2>

<p>Psiholoģiskā izpēte ir veids, kurā tiek izmantotas dažādas psiholoģiskās izpētes metodikas un testus, lai izzinātu cilvēka intelektu, personību, emocijas, attiecības. Psiholoģisko izpēti veic individuāli, gan pieaugušajiem, gan bērniem. 
Visbiežāk psiholoģisko izpēti nozīmē kāds speciālists – ģimenes ārsts, psihiatrs, neirologs, bāriņtiesas darbinieks, policists, advokāts u.c. Tomēr arī cilvēks individuāli var ierosināt veikt izpēti, lai labāk iepazītu kādus savas personības aspektus, piemēram, noskaidrot intelekta spējas. 
Bieži interesi par psiholoģisko izpēti izrāda gan pirmsskolas vecuma bērnu, gan skolēnu vecāki, ar vēlmi noskaidrot savu bērnu vadošās intelektuālās spējas vai to grūtības, mācīšanās iemaņas, vēlamāko apmācības veidu un programmu.
</p>
Psiholoģiskās izpētes mērķi, piemēram:
<ul>
    <li>	Cilvēka intelektuālās sfēras izpēte - uzmanību, atmiņu, domāšanas īpatnības, stiprās un vājās puses; </li>
<li>	Psihisko procesu darbības raksturojums, traucējumi;</li>
<li>	Personības vadošo emociju, raksturiezīmju, vajadzību, resursus raksturojums, u. c.;</li>
<li>	Skolēnu mācīšanās iemaņas, palīdzības iespējas mācību vielas sekmīgākai apguvei, utt.</li>
</ul>