
<h2>Masāža zīdaiņiem, bērniem un pieaugušajiem</h2>

Nodarbībā ietilpst:
<ul>
    <li>	masāža klasiskajā izpratnē </li>
<li>	pozicionēšana un vingrošana pēc Bobata (Bobath) metodes</li>
<li>	vecāku apmācība - hendlings (handling) un masāžas pamatprincipi</li>
<li>	konsultācijas dažādos ar zīdaiņa kopšanu saistītos jautājumos</li>
<li></ul>
<p>
 Vesela bērna masāža
– psihomotorās attīstības veicināšana, zīdaiņa muskulatūras tonusa stabilizēšana, Handling metode (bērna attīstību veicinošas ikdienas aktivitātes: pareiza celšana, likšana, nešana, turēšana, ģērbšana, guldīšana u.c.), vecāku apmācība
 Bērna masāža pēc ārsta norādījuma
– pie neiroloģiskām (t. sk. muskulatūras tonusa traucējumiem zīdaiņiem), traumatoloģiskām (t. sk. dzemdību traumas), ortopēdiskām (t. sk. gūžu locītavas attīstības displāzijas), pulmonoloģiskām, ģenētiskām, endokrinoloģiskām un citām saslimšanām

</p>