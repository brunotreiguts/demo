<h2>ANGĻU VALODAS TEKSTAM PAREDZĒTAIS SKATS</h2>
<p>
Spēle ar smiltīm kā bērna pašapzināšānās attīstības process un spontānā ‘’pašterapija” ir sen zināma. Katrs varam atcerēties savas izjūtas smilšu pludmalē - prieku par iespēju tajās nogulties, ierakties vai vienkārši sijāt smilšu strūklu caur pirkstiem. Smiltis provocē būvēt pilis, cietokšņus un pazemes ejas, līdz ūdens vilnis to noskalo vai kāda neuzmanīga pēda iemanās sagraut, tomēr tas neizraisa apjukumu vai aizvainojumu, bet gluži otrādi - rodas vēl lielāks azarts (entuziasms) ķerties klāt pie jaunā būvēšanas un kļūt par pasaules radītāju. 
Vēsturiski sākotnēji smilšu spēles metodi vairāk attiecināja un pielietoja darbam ar bērniem. Taču laikā gaitā tā kļuvusi par radošu pašterapijas metodi, ko pielieto arī darbā ar pieaugušajiem. Spēle ar smiltīm un miniatūrām figūrām palīdz pietuvoties un ielūkoties cilvēka iekšējā emocionālajā pasaulē - izprast vēlmes, tikt galā ar atmiņām, atklāt traumējošus notikumus un pieredzes, atvērt personības iekšējo spēku resursus un dažreiz ieraudzīt veiksmīgāku risinājumu kādai sarežģītai situācijai. Iemācoties rast harmoniju smilšu bildē, tiek iegūta pieredze veiksmīgākai mijiedarbībai ar apkārtējo pasauli. 
</p><p>
Smilšu spēles terapijas laikā notiek saudzīgs pašdziedināšanās process, kad vārdos grūti aprakstāmās un neapzinātās jūtas un domas tiek izpaustas caur tēlu un simbolu valodu. Dziedinošs ir smilšu bildes veidošanas process. Blakus esošais terapeits rada pasargātu un atbalstošu vidi, kas veicina uzticēšanos un paļaušanos dziednieciskajam procesam. Smilšu spēle var šķist it kā nenopietna parotaļāšanās, taču būtībā tas ir nopietns un atbildīgs darbs dvēseles dziļāko jūtu līdzsvarošanā (sakārtošanā) un dziedināšanā.  
</p>

Smilšu spēļu terapija var palīdzēt gadījumos, ja ir:
<ul>
    <li>	Traumatiski pārdzīvojumi; </li>
<li>	Emocionālas problēmas;</li>
<li>	Grūtības saprast sevi un citus, veidot attiecības ar citiem;</li>
<li>	Ģimenes attiecību problēmas; </li>
<li>	Neirozes, stostīšanās, tiki;</li>
<li>	Somatiskās saslimšanas, kuru pamatā ir psihisks konflikts;</li>
<li>	Attīstības aizture;</li>
<li>	Nakts bailes, murgi, fobijas;</li>
<li>	Mācību grūtības skolā;</li>
<li>	Hiperaktivitāte;</li>
<li>	Depresija;</li>
<li>	Stresa pārvarēšanā un relaksācijā.</li>
</ul>
<p>
Smilšu spēles terpiju var pielietot bērniem sākot no 4 gadu vecuma līdz pat pieaugušam vecumam. 
Smilšu spēles sesija notiek 1 reizi nedēļā un ilgst 45-55 min.
</p><p>
Ja darbs ir ar bērnu, tad vecākiem jārēķinās, ka arī viņiem šajā procesā jāiesaistās. Parasti, pirms uzsāk darbu ar bērnu, speciālistam ir nepieciešama saruna ar vecākiem, lai pārrunātu jautājumus, kas skar bērna attīstību un grūtības, ar kurām bērns vai ģimene ir saskārusies, kā arī vecāku gaidas no terapijas procesa. Tāpat arī procesa laikā periodiski notiek konsultācijas vecākiem.
</p>